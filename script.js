const productsContainer = document.getElementById('products');

function displayProducts(products) {
    productsContainer.innerHTML = '';
    products.forEach(product => {
        const productCard = document.createElement('div');
        productCard.className = 'product-card';

        productCard.innerHTML = `
            <img src="${product.imageUrl}" alt="${product.description}">
            <div class="product-info">
                <h2>${product.description}</h2>
                <p class="price">${product.discountPrice} ${product.currency}</p>
                ${product.discount !== '0%' ? `<p class="discount">${product.originPrice} ${product.currency}</p>` : ''}
                <div class="buttons">
                    <a href="${product.promotionUrl}" class="promotion-link" target="_blank">לקנייה</a>
                    <a href="#" class="share-link" onclick="shareProduct('${product.description}', '${product.promotionUrl}')">לשיתוף</a>
                </div>
            </div>
        `;

        productsContainer.appendChild(productCard);
    });
}

function sortProducts(by, order) {
    const sortedProducts = [...products].sort((a, b) => {
        const priceA = by === 'discountPrice' ? parseFloat(a.discountPrice) : parseFloat(a.originPrice);
        const priceB = by === 'discountPrice' ? parseFloat(b.discountPrice) : parseFloat(b.originPrice);

        return order === 'asc' ? priceA - priceB : priceB - priceA;
    });

    displayProducts(sortedProducts);
}

function shareProduct(description, url) {
    const shareData = {
        title: 'מחשבים ניידים למכירה',
        text: description,
        url: url
    };

    try {
        navigator.share(shareData)
            .then(() => console.log('Successful share'))
            .catch(error => console.log('Error sharing', error));
    } catch (error) {
        alert('שיתוף לא נתמך בדפדפן זה.');
    }
}

